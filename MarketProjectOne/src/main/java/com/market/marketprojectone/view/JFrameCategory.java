    public void fillCategoryTable() {
        try {
            LinkedHashMap<String, Function<Categories, Object>> cols = new LinkedHashMap<>();
            cols.put("ID", cat -> String.valueOf(cat.getId()));
            cols.put("Name", Categories::getName);
            cols.put("Descripción", Categories::getDescription);
            
            SwingUtils.fillTable(jTableCategory, categoryController.findAll(), cols);
        } catch (Exception e) {
            MessageUtils.showErrorMessage("Ha ocurrido un error al rellenar la tabla: " + e.getMessage());
        }
    }

