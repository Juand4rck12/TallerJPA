    private void jButtonInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInsertActionPerformed
        try {
            Categories category = new Categories();
            category.setName(jTextFieldName.getText());
            category.setDescription(jTextAreaDescription.getText());
            categoryController.insert(category);
            fillCategoryTable();
        } catch (Exception e) {
            MessageUtils.showErrorMessage("Ocurrio un error al insertar la categoria " + e.getMessage());
        }
    }//GEN-LAST:event_jButtonInsertActionPerformed

    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed
        try {
            Categories category = new Categories();
            category.setId(Long.valueOf(jTextFieldId.getText()));
            category.setName(jTextFieldName.getText());
            category.setDescription(jTextAreaDescription.getText());

            categoryController.update(category);
            MessageUtils.showInfoMessage("Categoria actualizada exitosamente");
            fillCategoryTable();
        } catch (Exception e) {
            MessageUtils.showErrorMessage("Ocurrio un error al actualizar la categoria: " + e.getMessage());
        }
    }//GEN-LAST:event_jButtonUpdateActionPerformed

    private void jTableCategoryMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableCategoryMouseClicked
        int rowSelected = jTableCategory.getSelectedRow();
        if (rowSelected != -1) {
            Long idCategory = Long.valueOf(jTableCategory.getValueAt(rowSelected, 0).toString());
            try {
                Categories categoryExists = categoryController.findByid(idCategory);
                jTextFieldId.setText(String.valueOf(categoryExists.getId()));
                jTextFieldName.setText(categoryExists.getName());
                jTextAreaDescription.setText(categoryExists.getDescription());
                
            } catch (Exception e) {
                MessageUtils.showErrorMessage("Ocurrio un error al usar el evento de clic " + e.getMessage());
            }
        }
    }//GEN-LAST:event_jTableCategoryMouseClicked

